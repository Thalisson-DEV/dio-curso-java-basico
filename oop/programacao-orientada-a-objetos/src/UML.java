public class UML {
    public static void main(String[] args) {
        /*
         * =========================================
         * UML
         * =========================================
         * Linguagem de Modelagem Unificada ou UML é uma notação que possibilita a representação gráfica da sua aplicação.
         * - Diagrama de classe: O Diagrama de classes é utilizado para representação de estruturas de classes de negócio,
         * "interfaces" e outros componentes do sistema. Por essa característica, este diagrama é considerado o mais importante para a UML,
         * pois auxilia a maioria dos demais diagramas.
         * - Diagrama de objetos: este diagrama representa os objetos existentes num determinado instante ou fato da sua aplicação.
         *
         * Num diagrama a estrutura das classes é constituída por:
         * Identificação: Nome e/ou finalidade da classe.
         * Atributos: Propriedades e/ou características.
         * Operações: Ações e/ou métodos.
         *
         * Relacionamentos:
         * Num diagrama as classes podem existir de forma independente, mas em algum momento da sua diagramação alguma classe pode e vai depender da outra.
         *  - Associação direcional
         *  - Associação bidirecional
         *  - Agregação: Numa agregação a classe principal contem uma relação com outra classe, mas ela pode existir sem a classe agregadora.
         *  - Composição: A composição caracteriza uma dependência existencial entra a classe associada.
         *
         * Visibilidade:
         * Os atributos e métodos de uma classe podem receber níveis de visibilidade, e na UML existem simbolos que os representam.
         * (+) Visibilidade pública
         * (#) Visibilidade protegida (muito associado com herança)
         * (-) Visibilidade privada
         */
    }
}
